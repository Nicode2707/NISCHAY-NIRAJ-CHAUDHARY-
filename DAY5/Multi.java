public class mult {

    public static int multiply(int num1,int num2 ){
        int multi = num1*num2;
        return multi;
    }
    public static void main(String[] args) {
        int a=4;
        int b=6;
        int prod = multiply(a,b);
        System.out.println("product of a and b" + prod);
    }
}
